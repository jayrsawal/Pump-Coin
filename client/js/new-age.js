function setProgressBar(n) {
  var eth_prog = (n/1000) * 100;
  $("#bar p span").html(eth_prog.toFixed(2));
  $("#bar").css("width",eth_prog+"%");
}

function setNumbers() {
  if(loaded) return;
  $.get("/smart-contract", function(data) {
    var wei_raised = parseInt(data["result"]);
    var eth_raised = wei_raised/1000000000000000000.0;
    var bag_dist = eth_raised * 100000;
    setNumber($("#bag-dist span"), bag_dist);
    setNumber($("#eth-raised span"), eth_raised);
    setProgressBar(eth_raised);
    loaded = true;
  });
  
}

function setNumber($elem, bag_dist) {
  var bag = 0.00, bag_add = 0.01;
  if(bag_dist == 0) return;
  bag = Math.floor(bag_dist);
  if(bag == bag_dist) {
    bag--;
  }
  setInterval(function() {
    if(bag+bag_add >= bag_dist) {
      clearInterval(this);
      return;
    }
    bag += bag_add;
    $elem.html(Number(bag.toFixed(4)).toLocaleString("en"));
  }, 20);
}
